        </div>
        <div class="card-body">
            <p>O Relatório Resumido de Execução Orçamentária (RREO) evidencia o cumprimento das metas fiscais estabelecidas na Lei de Responsabilidade Fiscal (LRF). É publicado bimestralmente e deve conter um conjunto de anexos padronizados.</p>
            <div class="row g-4">
                <div class="col-md-6">
                    <h2 class="h6">Anexo 01 – Resumo da Execução Orçamentária</h2>
                    <p class="small text-muted">Apresenta as receitas e despesas orçamentárias realizadas, segregando categorias econômicas e fontes de recursos.</p>
                </div>
                <div class="col-md-6">
                    <h2 class="h6">Anexo 02 – Execução das Despesas por Função/Subfunção</h2>
                    <p class="small text-muted">Detalha a aplicação dos recursos por áreas de atuação governamental, permitindo identificar prioridades de gasto.</p>
                </div>
                <div class="col-md-6">
                    <h2 class="h6">Anexo 03 – Gestão Fiscal</h2>
                    <p class="small text-muted">Monitora limites de despesa com pessoal, dívida consolidada e operações de crédito.</p>
                </div>
                <div class="col-md-6">
                    <h2 class="h6">Anexo 04 – Restos a Pagar</h2>
                    <p class="small text-muted">Relaciona obrigações empenhadas e não pagas em exercícios anteriores, evidenciando a gestão do passivo.</p>
                </div>
                <div class="col-md-6">
                    <h2 class="h6">Anexo 05 – Receitas e Despesas Previdenciárias</h2>
                    <p class="small text-muted">Demonstra o equilíbrio financeiro e atuarial dos regimes próprios de previdência.</p>
                </div>
                <div class="col-md-6">
                    <h2 class="h6">Anexo 06 – Resultados Primário e Nominal</h2>
                    <p class="small text-muted">Apura a capacidade de geração de poupança para pagamento de juros e amortizações da dívida.</p>
                </div>
                <div class="col-md-6">
                    <h2 class="h6">Anexo 07 – Indicadores Fiscais</h2>
                    <p class="small text-muted">Apresenta indicadores consolidados como Receita Corrente Líquida (RCL) e Despesa Total com Pessoal.</p>
                </div>
            </div>
        </div>
    </div>
